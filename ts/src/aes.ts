const RCON = new Uint8Array([0x00, 0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36]);

function xtime(x: number): number {
  return ((x << 1) ^ ((x & 0x80) !== 0 ? 0x1b : 0x00)) & 0xff;
}

function mul(a: number, b: number): number {
  let result = 0;
  let aa = a;
  let bb = b;
  while (bb > 0) {
    if ((bb & 1) !== 0) {
      result ^= aa;
    }
    aa = xtime(aa);
    bb >>= 1;
  }
  return result & 0xff;
}

function subWord(word: Uint8Array): Uint8Array {
  return Uint8Array.from(word, (b) => SBOX[b]);
}

function rotWord(word: Uint8Array): Uint8Array {
  return Uint8Array.of(word[1]!, word[2]!, word[3]!, word[0]!);
}

function expandKey(key: Uint8Array): Uint8Array {
  if (key.length !== AES_BLOCK_SIZE) {
    throw new Error(`AES-128 requires a 16-byte key, got ${key.length}`);
  }
  const expanded = new Uint8Array(AES_BLOCK_SIZE * (NR + 1));
  expanded.set(key);
  const temp = new Uint8Array(4);

  let i = NK;
  while (i < NB * (NR + 1)) {
    temp.set(expanded.slice((i - 1) * 4, i * 4));
    if (i % NK === 0) {
      temp.set(subWord(rotWord(temp)));
      temp[0] ^= RCON[i / NK]!;
    }
    for (let j = 0; j < 4; j += 1) {
      expanded[i * 4 + j] = expanded[(i - NK) * 4 + j]! ^ temp[j]!;
    }
    i += 1;
  }
  return expanded;
}

function addRoundKey(state: Uint8Array, roundKey: Uint8Array): void {
  for (let i = 0; i < AES_BLOCK_SIZE; i += 1) {
    state[i] ^= roundKey[i]!;
  }
}

function subBytes(state: Uint8Array): void {
  for (let i = 0; i < AES_BLOCK_SIZE; i += 1) {
    state[i] = SBOX[state[i]!]!;
  }
}

function invSubBytes(state: Uint8Array): void {
  for (let i = 0; i < AES_BLOCK_SIZE; i += 1) {
    state[i] = INV_SBOX[state[i]!]!;
  }
}

function shiftRows(state: Uint8Array): void {
  const t = state.slice();
  state[1] = t[5]!;
  state[5] = t[9]!;
  state[9] = t[13]!;
  state[13] = t[1]!;

  state[2] = t[10]!;
  state[6] = t[14]!;
  state[10] = t[2]!;
  state[14] = t[6]!;

  state[3] = t[15]!;
  state[7] = t[3]!;
  state[11] = t[7]!;
  state[15] = t[11]!;
}

function invShiftRows(state: Uint8Array): void {
  const t = state.slice();
  state[1] = t[13]!;
  state[5] = t[1]!;
  state[9] = t[5]!;
  state[13] = t[9]!;

  state[2] = t[10]!;
  state[6] = t[14]!;
  state[10] = t[2]!;
  state[14] = t[6]!;

  state[3] = t[7]!;
  state[7] = t[11]!;
  state[11] = t[15]!;
  state[15] = t[3]!;
}

function mixColumns(state: Uint8Array): void {
  for (let c = 0; c < 4; c += 1) {
    const i = c * 4;
    const s0 = state[i]!;
    const s1 = state[i + 1]!;
    const s2 = state[i + 2]!;
    const s3 = state[i + 3]!;
    state[i] = (mul(s0, 2) ^ mul(s1, 3) ^ s2 ^ s3) & 0xff;
    state[i + 1] = (s0 ^ mul(s1, 2) ^ mul(s2, 3) ^ s3) & 0xff;
    state[i + 2] = (s0 ^ s1 ^ mul(s2, 2) ^ mul(s3, 3)) & 0xff;
    state[i + 3] = (mul(s0, 3) ^ s1 ^ s2 ^ mul(s3, 2)) & 0xff;
  }
}

function invMixColumns(state: Uint8Array): void {
  for (let c = 0; c < 4; c += 1) {
    const i = c * 4;
    const s0 = state[i]!;
    const s1 = state[i + 1]!;
    const s2 = state[i + 2]!;
    const s3 = state[i + 3]!;
    state[i] = (mul(s0, 0x0e) ^ mul(s1, 0x0b) ^ mul(s2, 0x0d) ^ mul(s3, 0x09)) & 0xff;
    state[i + 1] = (mul(s0, 0x09) ^ mul(s1, 0x0e) ^ mul(s2, 0x0b) ^ mul(s3, 0x0d)) & 0xff;
    state[i + 2] = (mul(s0, 0x0d) ^ mul(s1, 0x09) ^ mul(s2, 0x0e) ^ mul(s3, 0x0b)) & 0xff;
    state[i + 3] = (mul(s0, 0x0b) ^ mul(s1, 0x0d) ^ mul(s2, 0x09) ^ mul(s3, 0x0e)) & 0xff;
  }
}

function encryptBlock(block: Uint8Array, expandedKey: Uint8Array): Uint8Array {
  const state = block.slice();
  addRoundKey(state, expandedKey.slice(0, AES_BLOCK_SIZE));
  for (let round = 1; round < NR; round += 1) {
    subBytes(state);
    shiftRows(state);
    mixColumns(state);
    addRoundKey(state, expandedKey.slice(round * AES_BLOCK_SIZE, (round + 1) * AES_BLOCK_SIZE));
  }
  subBytes(state);
  shiftRows(state);
  addRoundKey(state, expandedKey.slice(NR * AES_BLOCK_SIZE, (NR + 1) * AES_BLOCK_SIZE));
  return state;
}

function decryptBlock(block: Uint8Array, expandedKey: Uint8Array): Uint8Array {
  const state = block.slice();
  addRoundKey(state, expandedKey.slice(NR * AES_BLOCK_SIZE, (NR + 1) * AES_BLOCK_SIZE));
  for (let round = NR - 1; round > 0; round -= 1) {
    invShiftRows(state);
    invSubBytes(state);
    addRoundKey(state, expandedKey.slice(round * AES_BLOCK_SIZE, (round + 1) * AES_BLOCK_SIZE));
    invMixColumns(state);
  }
  invShiftRows(state);
  invSubBytes(state);
  addRoundKey(state, expandedKey.slice(0, AES_BLOCK_SIZE));
  return state;
}

export function zeroPad(data: Uint8Array): Uint8Array {
  if (data.length % AES_BLOCK_SIZE === 0) {
    return data.slice();
  }
  const out = new Uint8Array(data.length + (AES_BLOCK_SIZE - (data.length % AES_BLOCK_SIZE)));
  out.set(data);
  return out;
}

export function aes128CbcEncryptZeroPadded(
  plaintext: Uint8Array,
  key: Uint8Array,
  iv: Uint8Array
): Uint8Array {
  if (iv.length !== AES_BLOCK_SIZE) {
    throw new Error(`AES-CBC requires a 16-byte IV, got ${iv.length}`);
  }
  const expandedKey = expandKey(key);
  const padded = zeroPad(plaintext);
  const out = new Uint8Array(padded.length);
  let prev = iv.slice();

  for (let off = 0; off < padded.length; off += AES_BLOCK_SIZE) {
    const block = padded.slice(off, off + AES_BLOCK_SIZE);
    for (let i = 0; i < AES_BLOCK_SIZE; i += 1) {
      block[i] ^= prev[i]!;
    }
    const encrypted = encryptBlock(block, expandedKey);
    out.set(encrypted, off);
    prev = encrypted;
  }
  return out;
}

export function aes128CbcDecrypt(
  ciphertext: Uint8Array,
  key: Uint8Array,
  iv: Uint8Array
): Uint8Array {
  if (iv.length !== AES_BLOCK_SIZE) {
    throw new Error(`AES-CBC requires a 16-byte IV, got ${iv.length}`);
  }
  if (ciphertext.length % AES_BLOCK_SIZE !== 0) {
    throw new Error(`Ciphertext length must be a multiple of 16, got ${ciphertext.length}`);
  }
  const expandedKey = expandKey(key);
  const out = new Uint8Array(ciphertext.length);
  let prev = iv.slice();

  for (let off = 0; off < ciphertext.length; off += AES_BLOCK_SIZE) {
    const block = ciphertext.slice(off, off + AES_BLOCK_SIZE);
    const decrypted = decryptBlock(block, expandedKey);
    for (let i = 0; i < AES_BLOCK_SIZE; i += 1) {
      out[off + i] = decrypted[i]! ^ prev[i]!;
    }
    prev = block;
  }
  return out;
}
